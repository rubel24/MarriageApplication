    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       user = jTextField1.getText();
       pass = jPasswordField1.getText();
       try{
           connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/marriage", "root", "");
           PreparedStatement ps = connection.prepareStatement("select password, gender from profile where userId=?");
           ps.setString(1, user);
           rs = ps.executeQuery();
           while(rs.next()){
               pass1 = rs.getString("password");
               gender = rs.getString("gender");
           }
       }catch(SQLException ex){
           ex.printStackTrace();
       }
       if(pass.equals(pass1)){
           JOptionPane.showMessageDialog(this, "Successfully Logged in");
           this.dispose();
           userloginWindow uw = new userloginWindow(user,gender);
       }else{
           JOptionPane.showMessageDialog(this, "Invalid username or password");
           jTextField1.setText(null);
           jPasswordField1.setText(null);
       }
    }//GEN-LAST:event_jButton1ActionPerformed

